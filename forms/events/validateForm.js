function validateForm(form){
	
	if(form.getValue("ramal") == "") {
		throw "Campo Ramal não foi preenchido.";
	}
	if(form.getValue("obs") == "") {
		throw "Campo Ramal não foi preenchido.";
	}
	if(form.getValue("txt_categoria") == "") {
		throw "Campo Ramal não foi preenchido.";
	}
	if(form.getValue("maquina") == "") {
		throw "Campo Ramal não foi preenchido.";
	}
	if(form.getValue("ramal") == "") {
		throw "Campo Ramal não foi preenchido.";
	}
	
}